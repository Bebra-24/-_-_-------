-- ============================================================
-- 2. СОЗДАНИЕ ТАБЛИЦ-СПРАВОЧНИКОВ
-- ============================================================

CREATE TABLE [Подразделения] (
    [КодПодразделения] INT IDENTITY(1,1) PRIMARY KEY,
    [Название] NVARCHAR(100) NOT NULL UNIQUE,
    [Описание] NVARCHAR(MAX) NULL
);
GO

CREATE TABLE [Отделы] (
    [КодОтдела] INT IDENTITY(1,1) PRIMARY KEY,
    [Название] NVARCHAR(100) NOT NULL UNIQUE,
    [Описание] NVARCHAR(MAX) NULL
);
GO

CREATE TABLE [Должности] (
    [КодДолжности] INT IDENTITY(1,1) PRIMARY KEY,
    [Название] NVARCHAR(100) NOT NULL UNIQUE,
    [Оклад] DECIMAL(10,2) NULL,
    [Описание] NVARCHAR(MAX) NULL
);
GO

CREATE TABLE [ТипыПосещений] (
    [КодТипа] INT IDENTITY(1,1) PRIMARY KEY,
    [Название] NVARCHAR(50) NOT NULL UNIQUE,
    [Описание] NVARCHAR(MAX) NULL
);
GO

CREATE TABLE [СтатусыПосещений] (
    [КодСтатуса] INT IDENTITY(1,1) PRIMARY KEY,
    [Название] NVARCHAR(50) NOT NULL UNIQUE,
    [Описание] NVARCHAR(MAX) NULL
);
GO

CREATE TABLE [Группы] (
    [КодГруппы] INT IDENTITY(1,1) PRIMARY KEY,
    [Название] NVARCHAR(50) NOT NULL UNIQUE,
    [Описание] NVARCHAR(MAX) NULL,
    [МаксимальноеКоличество] INT DEFAULT 10
);
GO

-- ============================================================
-- 3. СОЗДАНИЕ ТАБЛИЦЫ СОТРУДНИКИ (БЕЗ UNIQUE НА EMAIL)
-- ============================================================
CREATE TABLE [Сотрудники] (
    [КодСотрудника] INT PRIMARY KEY,
    [Фамилия] NVARCHAR(50) NOT NULL,
    [Имя] NVARCHAR(50) NOT NULL,
    [Отчество] NVARCHAR(50) NULL,
    [КодПодразделения] INT NULL,
    [КодОтдела] INT NULL,
    [КодДолжности] INT NULL,
    [ТелефонСлужебный] NVARCHAR(50) NULL,
    [EmailСлужебный] NVARCHAR(100) NULL,  -- Убрано UNIQUE
    [ДатаПриема] DATE NULL,
    [ДатаУвольнения] DATE NULL,
    [Активно] BIT DEFAULT 1,
    
    CONSTRAINT FK_Сотрудники_Подразделения FOREIGN KEY ([КодПодразделения]) 
        REFERENCES [Подразделения]([КодПодразделения]),
    CONSTRAINT FK_Сотрудники_Отделы FOREIGN KEY ([КодОтдела]) 
        REFERENCES [Отделы]([КодОтдела]),
    CONSTRAINT FK_Сотрудники_Должности FOREIGN KEY ([КодДолжности]) 
        REFERENCES [Должности]([КодДолжности]),
    CONSTRAINT CK_Сотрудники_ПодразделениеИлиОтдел CHECK (
        ([КодПодразделения] IS NOT NULL OR [КодОтдела] IS NOT NULL)
    )
);
GO

-- ============================================================
-- 4. ТАБЛИЦА ПОСЕТИТЕЛИ
-- ============================================================
CREATE TABLE [Посетители] (
    [КодПосетителя] INT IDENTITY(1,1) PRIMARY KEY,
    [Фамилия] NVARCHAR(50) NOT NULL,
    [Имя] NVARCHAR(50) NOT NULL,
    [Отчество] NVARCHAR(50) NULL,
    [НомерТелефона] NVARCHAR(50) NULL,
    [Email] NVARCHAR(100) NULL UNIQUE,
    [ДатаРождения] DATE NULL,
    [СерияПаспорта] NVARCHAR(4) NULL,
    [НомерПаспорта] NVARCHAR(6) NULL,
    [Логин] NVARCHAR(50) NOT NULL UNIQUE,
    [Пароль] NVARCHAR(100) NOT NULL,
    [ДатаРегистрации] DATE DEFAULT GETDATE(),
    [Активно] BIT DEFAULT 1,
    
    CONSTRAINT UQ_Посетители_Паспорт UNIQUE ([СерияПаспорта], [НомерПаспорта]),
    CONSTRAINT CK_Посетители_Паспорт CHECK (
        ([СерияПаспорта] IS NULL AND [НомерПаспорта] IS NULL) OR
        ([СерияПаспорта] LIKE '[0-9][0-9][0-9][0-9]' AND 
         [НомерПаспорта] LIKE '[0-9][0-9][0-9][0-9][0-9][0-9]')
    )
);
GO

-- ============================================================
-- 5. ТАБЛИЦА ПОСЕЩЕНИЯ
-- ============================================================
CREATE TABLE [Посещения] (
    [КодПосещения] INT IDENTITY(1,1) PRIMARY KEY,
    [КодПосетителя] INT NOT NULL,
    [КодСотрудника] INT NOT NULL,
    [КодТипа] INT NOT NULL,
    [КодСтатуса] INT NOT NULL,
    [КодГруппы] INT NULL,
    [ДатаПосещения] DATE NOT NULL DEFAULT GETDATE(),
    [ВремяВхода] TIME NULL,
    [ВремяВыхода] TIME NULL,
    [ЦельВизита] NVARCHAR(MAX) NULL,
    [Примечание] NVARCHAR(MAX) NULL,
    
    CONSTRAINT FK_Посещения_Посетители FOREIGN KEY ([КодПосетителя]) 
        REFERENCES [Посетители]([КодПосетителя]),
    CONSTRAINT FK_Посещения_Сотрудники FOREIGN KEY ([КодСотрудника]) 
        REFERENCES [Сотрудники]([КодСотрудника]),
    CONSTRAINT FK_Посещения_ТипыПосещений FOREIGN KEY ([КодТипа]) 
        REFERENCES [ТипыПосещений]([КодТипа]),
    CONSTRAINT FK_Посещения_СтатусыПосещений FOREIGN KEY ([КодСтатуса]) 
        REFERENCES [СтатусыПосещений]([КодСтатуса]),
    CONSTRAINT FK_Посещения_Группы FOREIGN KEY ([КодГруппы]) 
        REFERENCES [Группы]([КодГруппы])
);
GO

