-- 10. ПРЕДСТАВЛЕНИЯ ДЛЯ ОТЧЁТОВ
-- ============================================================
CREATE VIEW [v_ВсеПосещения] AS
SELECT 
    п.[КодПосещения],
    пос.[Фамилия] + ' ' + пос.[Имя] + ISNULL(' ' + пос.[Отчество], '') AS [Посетитель],
    пос.[Логин],
    сотр.[Фамилия] + ' ' + сотр.[Имя] AS [Сотрудник],
    тип.[Название] AS [Тип],
    статус.[Название] AS [Статус],
    гр.[Название] AS [Группа],
    п.[ДатаПосещения]
FROM [Посещения] п
JOIN [Посетители] пос ON п.[КодПосетителя] = пос.[КодПосетителя]
JOIN [Сотрудники] сотр ON п.[КодСотрудника] = сотр.[КодСотрудника]
JOIN [ТипыПосещений] тип ON п.[КодТипа] = тип.[КодТипа]
JOIN [СтатусыПосещений] статус ON п.[КодСтатуса] = статус.[КодСтатуса]
LEFT JOIN [Группы] гр ON п.[КодГруппы] = гр.[КодГруппы];
GO

